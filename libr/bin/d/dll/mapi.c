typedef int (*GperfForeachCallback)(void *user, const char *k, const char *v);
int gperf_mapi_foreach(GperfForeachCallback cb, void *user) {
  int i = 0; while (kvs[i].name) {
  cb (user, kvs[i].name, kvs[i].value);
  i++;}
  return 0;
}
const char *gperf_mapi_get(const char *s) {
  int i = 0; while (kvs[i].name) {
  if (!strcmp (s, kvs[i].name)) return kvs[i].value;
  i++;}
  return NULL;
}
#define sdb_hash_c_mapi(x,y) gperf_mapi_hash(x)
const unsigned int gperf_mapi_hash(const char *s) {
  int sum = strlen (s);
  while (*s) { sum += *s; s++; }
  return sum;
}
struct {const char *name;void *get;void *hash;void *foreach;} gperf_mapi = {
  .name = "mapi",
  .get = &gperf_mapi_get,
  .hash = &gperf_mapi_hash,
  .foreach = &gperf_mapi_foreach
};

