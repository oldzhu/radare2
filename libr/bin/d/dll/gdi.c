typedef int (*GperfForeachCallback)(void *user, const char *k, const char *v);
int gperf_gdi_foreach(GperfForeachCallback cb, void *user) {
  int i = 0; while (kvs[i].name) {
  cb (user, kvs[i].name, kvs[i].value);
  i++;}
  return 0;
}
const char *gperf_gdi_get(const char *s) {
  int i = 0; while (kvs[i].name) {
  if (!strcmp (s, kvs[i].name)) return kvs[i].value;
  i++;}
  return NULL;
}
#define sdb_hash_c_gdi(x,y) gperf_gdi_hash(x)
const unsigned int gperf_gdi_hash(const char *s) {
  int sum = strlen (s);
  while (*s) { sum += *s; s++; }
  return sum;
}
struct {const char *name;void *get;void *hash;void *foreach;} gperf_gdi = {
  .name = "gdi",
  .get = &gperf_gdi_get,
  .hash = &gperf_gdi_hash,
  .foreach = &gperf_gdi_foreach
};

